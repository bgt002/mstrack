const api = (path) => `https://maplestory.io/api/${REGION}/${VERSION}${path}`;

async function search(term) {
  const res = await fetch(api(`/mob?searchFor=${encodeURIComponent(term)}`), {
    headers: { "User-Agent": UA },
    signal: AbortSignal.timeout(20000),
  });
  if (!res.ok) return [];
  return res.json();
}

async function tryIcon(id) {
  const res = await fetch(api(`/mob/${id}/icon`), {
    headers: { "User-Agent": UA },
    signal: AbortSignal.timeout(20000),
  });
  if (!res.ok) return null;
  const type = res.headers.get("content-type") || "";
  if (!type.startsWith("image/")) return null;
  const buf = Buffer.from(await res.arrayBuffer());
  return buf.length > 500 ? buf : null;
}

function rankCandidates(results, term) {
  const t = term.toLowerCase();
  return results
    .filter((m) => m && m.isBoss)
    .map((m) => {
      const name = (m.name || "").toLowerCase();
      let score = 0;
      if (name === t) score += 100;
      if (name.includes(t)) score += 40;
      if (m.mobType === "7N") score += 5; // final phase tends to have full art
      return { ...m, score };
    })
    .sort((a, b) => b.score - a.score)
    .slice(0, 12);
}

async function main() {
  await mkdir(OUT_DIR, { recursive: true });
  const have = [];
  const missing = [];

  for (const [bossId, term] of Object.entries(SEARCH)) {
    process.stdout.write(`• ${bossId} (${term}) … `);
    try {
      const results = await search(term);
      const candidates = rankCandidates(results, term);
      let saved = false;
      for (const c of candidates) {
        const buf = await tryIcon(c.id);
        if (buf) {
          await writeFile(join(OUT_DIR, `${bossId}.png`), buf);
          console.log(`ok  id=${c.id} "${c.name}" ${(buf.length / 1024) | 0}kb`);
          have.push(bossId);
          saved = true;
          break;
        }
      }
      if (!saved) {
        console.log(`MISS (${candidates.length} candidates, no icon)`);
        missing.push(bossId);
      }
    } catch (e) {
      console.log(`ERROR ${e.message}`);
      missing.push(bossId);
    }
  }

  const manifest =
    "// AUTO-GENERATED by scripts/fetch-boss-images.mjs — do not edit by hand.\n" +
    "// Boss ids that have an icon at /public/bosses/<id>.png\n" +
    `export const BOSS_IMAGE_IDS = new Set<string>(${JSON.stringify(
      have.sort()
    )});\n\n` +
    "export function bossImage(id: string): string | null {\n" +
    "  return BOSS_IMAGE_IDS.has(id) ? `/bosses/${id}.png` : null;\n" +
    "}\n";
  await writeFile(join(ROOT, "src", "lib", "bossImages.ts"), manifest);

  console.log(`\nDone. ${have.length} icons, ${missing.length} missing.`);
  if (missing.length) console.log("Missing:", missing.join(", "));
}

main();
